public class Ex_31 {
    public static void main(String[] args) {
        // Declaração e inicialização do array de strings
        String[] frutas = {"pera", "uva", "morango", "pessego", "abacate"};

        // Loop para imprimir todos os elementos do array
        for (int i = 0; i < frutas.length; i++) {
            System.out.println("Elemento " + i + ": " + frutas[i]);
        }
    }
}
