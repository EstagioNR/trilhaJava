// Impressão de Números: Escreva um programa para imprimir números de 1 a 10 (ou qualquer intervalo fornecido) usando um loop for.

import java.util.Scanner;

public class ExR1_17 {

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        scanner.close();
    }
}




